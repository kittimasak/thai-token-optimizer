
const { encoding_for_model } = require('@dqbd/tiktoken');
const { encode } = require('gpt-tokenizer/model/gpt-4');
const fs = require('fs');

console.log('--- node_modules Stress Test ---');

function generateMegaPrompt(sizeMb = 2) {
    const baseThai = "การทดสอบการบีบอัดโทเคนภาษาไทยด้วยระบบ Thai Token Optimizer v2.0 เพื่อให้มั่นใจว่าสามารถทำงานได้อย่างถูกต้องและรวดเร็วที่สุดในทุกสถานการณ์ ";
    const baseEng = "Testing Thai Token Optimizer v2.0 compression with massive payloads to ensure stability, performance, and memory efficiency under heavy load. ";
    const iterations = (sizeMb * 1024 * 1024) / (baseThai.length + baseEng.length);
    let mega = "";
    for (let i = 0; i < iterations; i++) {
        mega += baseThai + baseEng;
    }
    return mega;
}

const text = generateMegaPrompt(5); // 5MB payload
console.log(`Payload size: ${(text.length / 1024 / 1024).toFixed(2)} MB`);

// 1. Tiktoken Test
console.log('\n[1/2] Testing @dqbd/tiktoken (Full Capacity)...');
const startTik = Date.now();
const enc = encoding_for_model("gpt-4");
const tokensTik = enc.encode(text);
const endTik = Date.now();
console.log(`- Tokenized into ${tokensTik.length} tokens`);
console.log(`- Time: ${endTik - startTik}ms`);
console.log(`- Throughput: ${((tokensTik.length / (endTik - startTik)) * 1000).toFixed(0)} tokens/sec`);
enc.free();

// 2. gpt-tokenizer Test
console.log('\n[2/2] Testing gpt-tokenizer (Full Capacity)...');
const startGpt = Date.now();
const tokensGpt = encode(text);
const endGpt = Date.now();
console.log(`- Tokenized into ${tokensGpt.length} tokens`);
console.log(`- Time: ${endGpt - startGpt}ms`);
console.log(`- Throughput: ${((tokensGpt.length / (endGpt - startGpt)) * 1000).toFixed(0)} tokens/sec`);

console.log('\n--- Memory Usage ---');
console.log(JSON.stringify(process.memoryUsage(), null, 2));

console.log('\n[Status] node_modules STRESS TEST PASS (No crashes, healthy throughput)');
